te zijn. 
</p>
<h2 class="western">Artikel 15: Klachtplicht 
</h2>
<p style="margin-bottom: 0in">15.1 De consument kan op een gebrek in
de prestatie geen beroep meer doen, als hij hierover niet binnen
bekwame tijd nadat hij het gebrek heeft ontdekt of redelijkerwijs had
behoren te ontdekken schriftelijk bij de ondernemer heeft geklaagd. 
</p>
<p style="margin-bottom: 0in">15.2 De consument moet klachten over de
hoogte van het factuurbedrag, op straffe van verval van alle rechten,
binnen de betalingstermijn schriftelijk bij de ondernemer hebben
ingediend. Als de betalingstermijn langer is dan dertig dagen, moet
de consument uiterlijk binnen dertig dagen na factuurdatum
schriftelijk hebben geklaagd. 
</p>
<h2 class="western"> Artikel 17: Zekerheden 
</h2>
<p style="margin-bottom: 0in">17.1 Na levering blijft de ondernemer
eigenaar van geleverde zaken zolang de consument: 
</p>
<p style="margin-bottom: 0in">- tekortschiet of tekort zal schieten
in de nakoming van zijn verplichtingen uit deze overeenkomst of
andere overeenkomsten; 
</p>
<p style="margin-bottom: 0in">- voor verrichte of nog te verrichten
werkzaamheden uit zodanige overeenkomsten niet betaalt of zal
betalen; 
</p>
<p style="margin-bottom: 0in">- vorderingen die voortvloeien uit het
niet nakomen van bovengenoemde overeenkomsten, zoals schade, boete,
rente en kosten, niet heeft voldaan. 
</p>
<p style="margin-bottom: 0in">17.2 Zolang er op geleverde zaken een
eigendomsvoorbehoud rust, mag de consument deze niet bezwaren of
vervreemden. 
</p>
<p style="margin-bottom: 0in">17.3 Nadat de ondernemer zijn
eigendomsvoorbehoud heeft ingeroepen, mag hij de geleverde zaken
terughalen. De consument zal daartoe alle medewerking verlenen. 
</p>
<p style="margin-bottom: 0in">17.4 De ondernemer heeft op alle zaken
die hij uit welke hoofde ook onder zich heeft of zal krijgen en voor
alle vorderingen die hij op de consument heeft of mocht krijgen ten
opzichte van een ieder die daarvan afgifte verlangt een pandrecht en
een retentierecht. 
</p>
