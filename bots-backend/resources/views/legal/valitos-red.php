<h2 class="western">Artikel 17 Aansprakelijkheid en vrijwaring</h2>
<p style="margin-bottom: 0in">17.1 De opdrachtnemer is aansprakelijk
voor alle schade van welke aard dan ook in het kader</p>
<p style="margin-bottom: 0in">van de uitvoering van de opdracht
ontstaan. De opdrachtnemer is gehouden alle schade</p>
<p style="margin-bottom: 0in">die de opdrachtgever, de principaal
c.q. derden dientengevolge lijden te vergoeden.</p>
<p style="margin-bottom: 0in">De opdrachtnemer zal de opdrachtgever
vrijwaren voor aanspraken van derden ter zake</p>
<p style="margin-bottom: 0in">en zal de opdrachtgever zo nodig
schadeloos stellen.</p>
<p style="margin-bottom: 0in">17.2 De opdrachtnemer is aansprakelijk
voor administratieve boeten en/of andere</p>
<p style="margin-bottom: 0in">strafmaatregelen die worden opgelegd
aan de opdrachtgever, de principaal en/of derden</p>
<p style="margin-bottom: 0in">tengevolge van een doen en/of nalaten
van de opdrachtnemer.</p>
<p style="margin-bottom: 0in">17.3 De opdrachtnemer is aansprakelijk
voor schade aan hulpmaterialen, -constructies,</p>
<p style="margin-bottom: 0in">gereedschappen, materieel alsmede de
nog niet in het werk opgenomen zaken die door</p>
<p style="margin-bottom: 0in">de opdrachtnemer op de bouwplaats zijn
aangevoerd, waaronder schade door diefstal,</p>
<p style="margin-bottom: 0in">terugloop in waarde, vandalisme en
beschadiging met inbegrip van brandstichting.</p>
