<p style="margin-bottom: 0in">Indien [OPDRACHTNEMER] op verzoek van
opdrachtgever of met instemming van opdrachtgever, werkzaamheden of
andere prestaties heeft verricht die buiten de inhoud of omvang van
de overeenkomst vallen, zullen deze werkzaamheden of prestaties
door&nbsp;opdrachtgever worden vergoed volgens de gebruikelijke
tarieven van [OPDRACHTNEMER], indien geen andere prijsafspraken zijn
gemaakt. [OPDRACHTNEMER] is&nbsp;nimmer verplicht om aan een verzoek
om meer- of minderwerk te voldoen. [OPDRACHTNEMER] kan verlangen dat
daarvoor een afzonderlijke schriftelijke overeenkomst wordt gesloten.</p>
<p style="margin-bottom: 0in">Opdrachtgever aanvaardt dat door
werkzaamheden of prestaties als bedoeld in artikel 5.1
de&nbsp;overeengekomen doelstellingen en verwachtingen kunnen worden
beïnvloed.</p>
<p style="margin-bottom: 0in">Voor zover voor de dienstverlening een
vaste prijs is afgesproken zal [OPDRACHTNEMER] opdrachtgever&nbsp;
tevoren schriftelijk informeren over de financiële consequenties van
het meerwerk, tenzij dit onder de gegeven omstandigheden
redelijkerwijs niet mogelijk is.&nbsp;</p>
<p style="margin-bottom: 0in">Onder directe schade wordt uitsluitend
verstaan:</p>
<p style="margin-bottom: 0in">a. redelijke kosten die opdrachtgever
moet maken om de prestatie van [OPDRACHTNEMER] aan de overeenkomst te
laten beantwoorden; deze vervangende schade wordt echter niet vergoed
indien de overeenkomst door of op vordering van opdrachtgever wordt
ontbonden;</p>
<p style="margin-bottom: 0in">b. redelijke kosten gemaakt ter
vaststelling van de oorzaak en de omvang van de schade voor zover de
vaststelling betrekking heeft op directe schade in de zin van deze
voorwaarden; 
</p>
<p style="margin-bottom: 0in">c. redelijke kosten gemaakt ter
voorkoming of beperking van schade voor zover opdrachtgever aantoont
